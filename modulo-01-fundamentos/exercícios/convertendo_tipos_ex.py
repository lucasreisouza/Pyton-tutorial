# Escreva uma string representando um número inteiro.
# converta essa string para um inteiro, depois para um float, e imprima os valores e tipos.

numero_str = "17"
numero_int = int(numero_str)
numero_float = float(numero_int)

print(numero_str, type(numero_str))
print(numero_int, type(numero_int))
print(numero_float, type(numero_float))